// Stats counter animation
function animateCounter(element, target, duration = 2000) {
    let start = 0;
    const increment = target / (duration / 16);
    
    const timer = setInterval(() => {
        start += increment;
        if (start >= target) {
            element.textContent = target;
            clearInterval(timer);
        } else {
            element.textContent = Math.floor(start);
        }
    }, 16);
}

// Animate stats when they come into view
const statsObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            const statNumber = entry.target.querySelector('.stat-number');
            const text = statNumber.textContent;
            
            if (text.includes('K')) {
                const number = parseInt(text.replace('K+', ''));
                animateCounter(statNumber, number);
                statNumber.textContent = number + 'K+';
            } else if (text.includes('★')) {
                // Don't animate rating, just show it
                statNumber.style.opacity = '1';
            } else {
                // For other stats, just show them
                statNumber.style.opacity = '1';
            }
            
            statsObserver.unobserve(entry.target);
        }
    });
}, { threshold: 0.5 });

document.querySelectorAll('.stat').forEach(stat => {
    const statNumber = stat.querySelector('.stat-number');
    statNumber.style.opacity = '0';
    statNumber.style.transition = 'opacity 0.5s ease';
    statsObserver.observe(stat);
});

